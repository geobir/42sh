int		ft_strncmp(const char *s1, const char *s2, int n)
{
	while (*s1 == *s2 && n-- > 0)
	{
		if (*s1 == '\0' || n == 0)
			return (0);
		++s1;
		++s2;
	}
	if (*s1 > *s2)
		return (1);
	return (-1);
}
